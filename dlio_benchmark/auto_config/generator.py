import logging
from pathlib import Path

import yaml

from .confidence import Confidence, ParameterEstimate
from .extractor import DLIOSchema

logger = logging.getLogger(__name__)


def _val(est: ParameterEstimate | None):
    return est.value if est is not None else None


def _comment(est: ParameterEstimate | None) -> str:
    if est is None:
        return ""
    c = est.comment()
    return f"  # {c}" if c else ""


class _CommentedDumper(yaml.Dumper):
    """YAML dumper that can attach inline comments via tagged scalars."""
    pass


def generate_yaml(schema: DLIOSchema, output_path: str, workload_name: str = "auto_generated") -> None:
    """Emit a DLIO-compatible YAML config from the extracted schema.

    LOW/MEDIUM confidence values are annotated with inline comments.
    The output is plain YAML (not OmegaConf) so it works with DLIO's
    Hydra config loading without modification.
    """
    lines: list[str] = []

    def emit(key: str, value, est: ParameterEstimate | None = None, indent: int = 0):
        pad = "  " * indent
        comment = _comment(est)
        if isinstance(value, dict):
            lines.append(f"{pad}{key}:")
            for k, v in value.items():
                lines.append(f"{pad}  {k}: {v}")
        else:
            lines.append(f"{pad}{key}: {value}{comment}")

    lines.append(f"# Auto-generated DLIO config")
    lines.append(f"# Generated by dlio_benchmark.auto_config")
    if schema.format:
        lines.append(f"# Source format: {schema.format.value}  ({schema.format.source})")
    lines.append("")

    # --- model ---
    lines.append("model:")
    emit("name", workload_name, indent=1)
    lines.append("")

    # --- framework ---
    fw = _val(schema.framework)
    if fw:
        emit("framework", fw, schema.framework)
    lines.append("")

    # --- workflow ---
    lines.append("workflow:")
    emit("generate_data", "False", indent=1)
    emit("train", "True", indent=1)
    if _val(schema.do_checkpoint):
        emit("checkpoint", "True", schema.do_checkpoint, indent=1)
    lines.append("")

    # --- dataset ---
    lines.append("dataset:")
    if schema.data_folder:
        emit("data_folder", _val(schema.data_folder), schema.data_folder, indent=1)
    if schema.format:
        emit("format", _val(schema.format), schema.format, indent=1)
    if schema.num_files_train:
        emit("num_files_train", _val(schema.num_files_train), schema.num_files_train, indent=1)
    if schema.num_samples_per_file:
        emit("num_samples_per_file", _val(schema.num_samples_per_file), schema.num_samples_per_file, indent=1)
    if schema.record_length:
        emit("record_length", _val(schema.record_length), schema.record_length, indent=1)
    if schema.record_length_stdev and schema.record_length_stdev.value > 0:
        emit("record_length_stdev", _val(schema.record_length_stdev), schema.record_length_stdev, indent=1)
    lines.append("")

    # --- reader ---
    lines.append("reader:")
    if schema.framework:
        dl = "pytorch" if "pytorch" in schema.framework.value else "tensorflow"
        emit("data_loader", dl, indent=1)
    if schema.batch_size:
        emit("batch_size", _val(schema.batch_size), schema.batch_size, indent=1)
    if schema.read_threads:
        emit("read_threads", _val(schema.read_threads), schema.read_threads, indent=1)
    if schema.prefetch_size:
        emit("prefetch_size", _val(schema.prefetch_size), schema.prefetch_size, indent=1)
    if schema.file_shuffle:
        emit("file_shuffle", _val(schema.file_shuffle), schema.file_shuffle, indent=1)
    if schema.sample_shuffle:
        emit("sample_shuffle", _val(schema.sample_shuffle), schema.sample_shuffle, indent=1)
    lines.append("")

    # --- train ---
    lines.append("train:")
    if schema.epochs:
        emit("epochs", _val(schema.epochs), schema.epochs, indent=1)
    if schema.computation_time_mean:
        emit("computation_time", indent=1, value="")
        lines[-1] = lines[-1].rstrip()  # remove trailing space
        lines[-1] = f"  computation_time:"
        lines.append(f"    mean: {_val(schema.computation_time_mean)}{_comment(schema.computation_time_mean)}")
        if schema.computation_time_stdev:
            lines.append(f"    stdev: {_val(schema.computation_time_stdev)}{_comment(schema.computation_time_stdev)}")
    lines.append("")

    # --- checkpoint ---
    if _val(schema.do_checkpoint):
        lines.append("checkpoint:")
        if schema.checkpoint_interval_epochs:
            emit("epochs_between_checkpoints", _val(schema.checkpoint_interval_epochs),
                 schema.checkpoint_interval_epochs, indent=1)
        lines.append("")

    # Write out
    out = Path(output_path)
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text("\n".join(lines) + "\n")
    logger.info(f"Config written to {out}")

    # Print confidence summary
    fields_by_conf = {Confidence.LOW: [], Confidence.MEDIUM: [], Confidence.HIGH: []}
    for fname in vars(schema):
        est = getattr(schema, fname)
        if isinstance(est, ParameterEstimate):
            fields_by_conf[est.confidence].append(fname)
    if fields_by_conf[Confidence.LOW]:
        logger.warning(f"LOW confidence fields (verify manually): {fields_by_conf[Confidence.LOW]}")
    if fields_by_conf[Confidence.MEDIUM]:
        logger.info(f"MEDIUM confidence fields: {fields_by_conf[Confidence.MEDIUM]}")
